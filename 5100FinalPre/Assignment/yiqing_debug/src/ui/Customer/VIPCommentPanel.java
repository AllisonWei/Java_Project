    private void DetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DetailActionPerformed
        // TODO add your handling code here:
        int row = this.table.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(this,"Please select a row");
        }
        else{
            for(int i = 0;i < this.account.getRequestDirectory().getRequestList().size(); i++){
                if(this.account.getRequestDirectory().getRequestList().get(i).getType().equals("VIPComment") && this.account.getRequestDirectory().getRequestList().get(i).getId() == Integer.valueOf(table.getValueAt(row, 0).toString())){
                    CommentDetail cd = new CommentDetail(this.organization, this.account, (VIPComment)this.account.getRequestDirectory().getRequestList().get(i), this);
                    cd.setVisible(true);
                }
            }            
        }
    }//GEN-LAST:event_DetailActionPerformed


