    public void populate(){
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        int rowNumber = model.getRowCount();
        for(int i = 0; i < rowNumber;i++){
            model.removeRow(0);
        }
        for(int i = 0;i < this.account.getRequestDirectory().getRequestList().size(); i++){
            if(this.account.getRequestDirectory().getRequestList().get(i).getType().equals("VIPComment")){
                VIPComment vc = (VIPComment) this.account.getRequestDirectory().getRequestList().get(i);
                Object[] row = new Object[4];
                row[0] = vc.getId();
                SimpleDateFormat df = new SimpleDateFormat ("MM-dd HH:mm");
                row[1] = df.format(vc.getStartDate());
                row[2] = vc.getStatus();
                row[3] = vc.getComment();
                model.addRow(row);
            }
        }
    }
