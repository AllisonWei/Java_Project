    private void checkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkActionPerformed
        // TODO add your handling code here:
        String Password = new String(password.getPassword());
        if(this.account.getPassword().equals(Password)){
            Change.setVisible(true);
            lblName.setVisible(true);
            lblpass.setVisible(true);
            newName.setVisible(true);
            newPass.setVisible(true);
            tips.setVisible(true);
            check.setEnabled(false);
            password.setEnabled(false);
        }
        else{
            JOptionPane.showMessageDialog(this,"Password wrong");
        }
    }//GEN-LAST:event_checkActionPerformed

    private void ChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChangeActionPerformed
        // TODO add your handling code here:
        Boolean flag = true;
        if(newPass.getText().matches("^[ ]+$")){
            newPass.setForeground(Color.red);
            flag = false;
        }
        else{
            newPass.setForeground(Color.black);
        }
        if(newName.getText().matches("^[a-zA-Z ]+$")||newName.getText().equals("")){
            newName.setForeground(Color.black);
        }
        else{
            newName.setForeground(Color.red);
            flag = false;            
        }
        if(flag){
            if(!newName.getText().equals("")){
                this.account.setRealName(newName.getText());
            }
            if(!newPass.getText().equals("")){
                this.account.setPassword(newPass.getText());
            }
            JOptionPane.showMessageDialog(this,"Change success");
            this.setVisible(false);
            this.ca.InitData();
        }
        else{
            JOptionPane.showMessageDialog(this,"Input invalid");
        }

    }//GEN-LAST:event_ChangeActionPerformed

