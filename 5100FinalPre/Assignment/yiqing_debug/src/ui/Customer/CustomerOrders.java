    public void populate(int mode){
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        int rowNumber = model.getRowCount();
        for(int i = 0; i < rowNumber;i++){
            model.removeRow(0);
        }
        for(int i = 0; i < account.getRequestDirectory().getRequestList().size(); i++){
            
            if(mode == 0 && account.getRequestDirectory().getRequestList().get(i).getType().contains("Ticket")){
                Object[] row = new Object[4];
                row[0] = account.getRequestDirectory().getRequestList().get(i).getId();
                row[1] = account.getRequestDirectory().getRequestList().get(i).getOrganizationId();
                row[2] = account.getRequestDirectory().getRequestList().get(i).getType();
                SimpleDateFormat ft = new SimpleDateFormat ("MM-dd HH:mm");
                row[3] = ft.format(account.getRequestDirectory().getRequestList().get(i).getStartDate());
                model.addRow(row);
            }
            if(mode == 1 && (account.getRequestDirectory().getRequestList().get(i).getType().equals("VIP"))){
                Object[] row = new Object[4];
                row[0] = account.getRequestDirectory().getRequestList().get(i).getId();
                row[1] = account.getRequestDirectory().getRequestList().get(i).getOrganizationId();
                row[2] = account.getRequestDirectory().getRequestList().get(i).getType();
                SimpleDateFormat ft = new SimpleDateFormat ("MM-dd HH:mm");
                row[3] = ft.format(account.getRequestDirectory().getRequestList().get(i).getStartDate());
                model.addRow(row);                
            }
        }
    }
    
    
    
