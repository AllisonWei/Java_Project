    private void BtnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCancelActionPerformed
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(this,"Please select a row");
        }
        else{
            for(int i = 0; i < account.getRequestDirectory().getRequestList().size(); i++){
                if(account.getRequestDirectory().getRequestList().get(i).getId() == Integer.valueOf(table.getValueAt(row, 0).toString()) && account.getRequestDirectory().getRequestList().get(i).getOrganizationId().equals(table.getValueAt(row, 1).toString())){
                    account.getRequestDirectory().getRequestList().get(i).setStatus("Canceled");
                }
            }
            if(CbxOrderType.getSelectedItem().toString().equals("Normal")){
                this.populate(0);
            }
            else{
                this.populate(1);
            }
        }
    }//GEN-LAST:event_BtnCancelActionPerformed

