    private void initTrainline() {
        RailwayCompany railwayCompany = (RailwayCompany)origanization;
        List<request> requests = railwayCompany.getRequestDirectory().getRequestList();
        for (request request : requests) {
            if (request instanceof TrainLine
                && !request.getStatus().equals("Unsolved")
                && !request.getStatus().equals("Canceled")) {
                trainlineList.add(request);
            }
        }
    }
    
    private void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int size = model.getRowCount();
        for (int i = 0; i < size; i++) {
            model.removeRow(0);
        }
        for (request request : trainlineList) {
            TrainLine trainLine = (TrainLine)request;
            // "Route Number", "Departure Station", "Arriving Station", "Conductor", 
            // "Departure Date", "Route Type"
            Object[] row = new Object[7];
            row[0] = trainLine.getId();
            List<String> stationList = trainLine.getStationList();
            row[1] = null == stationList || stationList.isEmpty() ? "" : stationList.get(0);
            row[2] = null == stationList || stationList.size() < 2 ? "" : stationList.get(stationList.size() - 1);
            row[3] = trainLine.getCaptain() == null ? "" : trainLine.getCaptain().getName();
            SimpleDateFormat sdf = new SimpleDateFormat("MM-dd HH:mm");
            row[4] = sdf.format(trainLine.getStartDate() == null ? new Date() : trainLine.getStartDate());
            row[5] = trainLine.getType();
            row[6] = trainLine.getStatus();
            model.addRow(row);
        }
    }

