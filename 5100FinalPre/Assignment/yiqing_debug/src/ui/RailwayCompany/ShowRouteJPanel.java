    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int i = jTable1.getSelectedRow();
        if (i < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row.");
            return;
        }
        RailwayCompany railwayCompany = (RailwayCompany)origanization;
        List<request> requests = railwayCompany.getRequestDirectory().getRequestList();
        int id = Integer.parseInt(jTable1.getValueAt(i, 0).toString());
        for (request request : requests) {
            if (request instanceof TrainLine && request.getId() == id) {
                requests.remove(request);
                break;
            }
        }
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        trainlineList.remove(i);
        model.removeRow(i);
        DB4OUtil.getInstance().storeSystem(system);
        JOptionPane.showMessageDialog(null, "Information Deleted Succesfully!");
    }//GEN-LAST:event_jButton3ActionPerformed

