    private void initFlight() {
        AirlineCompany airLineCompany = (AirlineCompany)origanization;
        List<request> requests = airLineCompany.getRequestDirectory().getRequestList();
        for (request request : requests) {
            // Unsolved Canceled
            if (request instanceof Flight
                && !request.getStatus().equals("Unsolved")
                && !request.getStatus().equals("Canceled")) {
                flightList.add(request);
            }
        }
    }
    private void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int size = model.getRowCount();
        for (int i = 0; i < size; i++) {
            model.removeRow(0);
        }
        for (request request : flightList) {
            Flight flight = (Flight)request;
            Object[] row = new Object[7];
            row[0] = flight.getId();
            row[1] = flight.getStartAirport();
            row[2] = flight.getEndAirport();
            row[3] = null == flight.getCaptain() ? "" : flight.getCaptain().getName();
            SimpleDateFormat sdf = new SimpleDateFormat("MM-dd HH:mm");
            row[4] = sdf.format(flight.getStartDate() == null ? new Date() : flight.getStartDate());
            row[5] = flight.getType();
            row[6] = flight.getStatus();
            model.addRow(row);
        }
    }
    
