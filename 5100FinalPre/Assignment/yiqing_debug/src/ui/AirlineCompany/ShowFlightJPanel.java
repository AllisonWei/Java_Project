    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int i = jTable1.getSelectedRow();
        if (i < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row.");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int fightId = Integer.parseInt(model.getValueAt(i, 0).toString());
        AirlineCompany airLineCompany = (AirlineCompany)origanization;
        List<request> requests = airLineCompany.getRequestDirectory().getRequestList();
        for (request request : requests) {
            if (request instanceof Flight) {
                Flight flight = (Flight)request;
                if (flight.getId() == fightId) {
                    requests.remove(request);
                    break;
                }
            }
        }
        flightList.remove(i);
        model.removeRow(i);
        DB4OUtil.getInstance().storeSystem(system);
        JOptionPane.showMessageDialog(null, "Information Deleted Succesfully!");
    }//GEN-LAST:event_btnDeleteActionPerformed

