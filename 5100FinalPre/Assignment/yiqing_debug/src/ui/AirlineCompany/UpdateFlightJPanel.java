    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String id = txtFlightId.getText();
        String type = txtFlightType.getText();
        BoxHelper<Airplane> airplane = (BoxHelper<Airplane>)cmbPlaneId.getSelectedItem();
        String deAirport = (String)cmbDeAirport.getSelectedItem();
        String laAirport = (String)cmbLandAirport.getSelectedItem();
        BoxHelper<Employee> employee = (BoxHelper<Employee>)cmbCaptain.getSelectedItem();
        int ecoSeatNumber = Integer.parseInt(txtEcoNum.getText());
        int busSeatNumber = Integer.parseInt(txtBusiNum.getText());
        int firstSeatNumber = Integer.parseInt(txtFirstNum.getText());
        double ep = Double.parseDouble(txtEcoPrice.getText());
        double bp = Double.parseDouble(txtBusiPrice.getText());
        double fp = Double.parseDouble(txtFirstPrice.getText());
        String status = txtStatusField.getText();
        Date sDate;
        try {
            sDate = sdf.parse(txtDeDate.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Depature Date format error!");
            return;
        }
        
        // check
        AirlineCompany airLineCompany = (AirlineCompany)origanization;
        List<request> requests = airLineCompany.getRequestDirectory().getRequestList();
        for (request request : requests) {
            if (request instanceof Flight) {
                Flight flight = (Flight)request;
                if (this.flight.getId() != flight.getId() && flight.getId() == Integer.parseInt(id)) {
                    JOptionPane.showMessageDialog(null, "Flight number/ID already exists!");
                    return;
                }
                if (flight.getStartDate() != null && 
                    flight.getCaptain() != null &&
                    this.flight.getId() != flight.getId() &&
                    sdf.format(sDate).substring(0, 5).equals(sdf.format(flight.getStartDate()).substring(0, 5))
                    && employee.getData().getId() == flight.getCaptain().getId()) {
                    JOptionPane.showMessageDialog(null, "The same person can only arrange one shift a day!");
                    return;
                }
                
                
            }
        }
        if (deAirport.equals(laAirport)) {
            JOptionPane.showMessageDialog(null, "Departure airport and Langding airport cannot same!");
            return;
        }
        flight.setId(Integer.parseInt(id));
        flight.setRequestPersonId(employee.getId());
        flight.setOrganizationId(origanization.getId());
        flight.setType(type);
        flight.setFlight(deAirport, laAirport, airplane.getData(), employee.getData(), 
            ecoSeatNumber, busSeatNumber, firstSeatNumber, 
            ep, bp, fp);
        flight.setStartDate(sDate);
        flight.setStatus(status);
        DB4OUtil.getInstance().storeSystem(system);
        // Information Added Succesfully!
        JOptionPane.showMessageDialog(null, "Information Added Succesfully!");
    }//GEN-LAST:event_btnUpdateActionPerformed

