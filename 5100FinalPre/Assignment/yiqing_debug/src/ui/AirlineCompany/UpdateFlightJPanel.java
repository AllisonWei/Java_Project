    private void getAirPortList() {
        ArrayList<Enterprise> enterpriseList = system.getEnterpriseDirectory().getEnterpriseList();
        for (Enterprise enterprise : enterpriseList) {
            if (enterprise.getName().equals("Airport")) {
                List<Organization> orgList = enterprise.getOrganizationDirectory().getOrganizationList();
                airportList = new BoxHelper[orgList.size()];
                for (int i = 0; i < orgList.size(); i++) {
                    Airport airport = (Airport)orgList.get(i);
                    airportList[i] = new BoxHelper<>(airport.getId(), airport);
                }
            }
        }
    }
    
    private void getEmployeeList() {
        AirlineCompany airLineCompany = (AirlineCompany)origanization;
        List<Employee> employeeList = new ArrayList<>();
        for (Employee employee : airLineCompany.getEmployeeDirectory().getEmployeeList()) {
            if (employee.getRole().getRole().equals("AirCaptain")) {
                employeeList.add(employee);
            }
        }
        airCaptainList = new BoxHelper[employeeList.size()];
        for (int i = 0; i < employeeList.size(); i++) {
            airCaptainList[i] = new BoxHelper<>(employeeList.get(i).getName(), employeeList.get(i));
        }
    }

