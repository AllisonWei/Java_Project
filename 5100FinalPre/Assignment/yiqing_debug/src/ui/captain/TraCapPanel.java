    private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();
        int size = model.getRowCount();
        for (int i = 0; i < size; i++) {
            model.removeRow(0);
        }
        List<request> requests = railwayCompany.getRequestDirectory().getRequestList();
        SimpleDateFormat sdf = new SimpleDateFormat("MM-dd hh:mm");
        for (request request : requests) {
            TrainLine trainLine = (TrainLine)request;
            if(trainLine.getCaptain().getName().equals(account.getName())){
            Object[] row = new Object[6];
            row[0] = trainLine;
            row[1] = trainLine.getId();
            row[2] = trainLine.getCurrentStation();
            row[3] = trainLine.getNextStation();
            row[4] = trainLine.getStartDate() == null ? "" : sdf.format( trainLine.getStartDate());
            row[5] = trainLine.getEndDate() == null ? "" : sdf.format( trainLine.getEndDate());
            model.addRow(row);
            }
        }
    }

