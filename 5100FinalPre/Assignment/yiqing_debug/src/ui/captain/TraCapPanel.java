    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
         int row = workRequestJTable.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        TrainLine trainLine = (TrainLine) workRequestJTable.getValueAt(row, 0);
        List<request> requestList = system.getOrganizationByNameAndType("Station", trainLine.getNextStation()).
                getRequestDirectory()
                .getRequestList();
        for (request req: requestList){
            if (!(req instanceof Entering)){
                continue;
            }
            Entering entering = (Entering) req;
            if (entering.getTrainLine() == trainLine){
                if(!trainLine.moveNextStation()){
                    JOptionPane.showMessageDialog(null, "This is the last station.", "Finished", JOptionPane.WARNING_MESSAGE);
                    trainLine.setStatus("Finished");
                    requestList = system.getOrganizationByIdAndType("RailwayCompany", trainLine.getOrganizationId()).
                        getRequestDirectory()
                        .getRequestList();
                    if(trainLine.getType().equals("Normal")){
                        for (request req1: requestList){
                            if (!(req1 instanceof TrainTicket)){
                            continue;
                            }
                            TrainTicket trainTicket = (TrainTicket) req;
                            if (trainTicket.getTrainLineId() == trainLine.getId()){
                                trainTicket.setStatus("Finished");
                            }
                        }
                    }
                }
                entering.Finish();
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed


