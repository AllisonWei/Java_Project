    private void processJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processJButtonActionPerformed
        int i = workRequestJTable.getSelectedRow();
        if (i < 0) {
            JOptionPane.showMessageDialog(null, "Please select a line.");
            return;
        }
        String status = (String)workRequestJTable.getModel().getValueAt(i, 3);
        if (!status.equals("Pending")) {
            return;
        }
        String id = (String)workRequestJTable.getModel().getValueAt(i, 4);
        OrderDto orderDto = OrderDBUtil.queryById(id);
        orderDto.setStatus("Finished");
        OrderDBUtil.update(orderDto);
        workRequestJTable.getModel().setValueAt("Finished", i, 3);
        JOptionPane.showMessageDialog(null, "Finshed.");
    }//GEN-LAST:event_processJButtonActionPerformed

