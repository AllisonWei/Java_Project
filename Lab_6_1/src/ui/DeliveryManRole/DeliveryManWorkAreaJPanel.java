    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();
        List<OrderDto> orderList = OrderDBUtil.queryByDelName(this.userDto.getUserName());
        for (int i = 0; i < orderList.size(); i++) {
            Object[] data = new Object[5];
            data[0] = orderList.get(i).getRestaurantName();
            data[1] = orderList.get(i).getMeal();
            data[2] = orderList.get(i).getUserName();
            data[3] = orderList.get(i).getStatus();
            data[4] = orderList.get(i).getId();
            model.addRow(data);
        }
    }

