    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // Get user name
        String userName = txtUserName.getText();
        // Get Password
        char[] passwordCharArray = pwdField.getPassword();
        String password = String.valueOf(passwordCharArray);
        UserDto userDto = UserDBUtil.queryByUserName(userName);
        if (null == userDto) {
            JOptionPane.showMessageDialog(null, "Invalid User Name/ Password.");
            return;
        } else if (!userDto.getPassword().equals(password)) {
            JOptionPane.showMessageDialog(null, "Invalid User Name/ Password.");
            return;
        }
        JPanel mainScreen = new MainScreen(mainWorkArea, userDto);
        mainWorkArea.add("MainScreen", mainScreen);
        CardLayout layout = (CardLayout) mainWorkArea.getLayout();
        layout.next(mainWorkArea);
    }//GEN-LAST:event_btnLoginActionPerformed


