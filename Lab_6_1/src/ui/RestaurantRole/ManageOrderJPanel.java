    public void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();
        int size = model.getRowCount();
        for (int i = 0; i < size; i++) {
            model.removeRow(0);
        }
        List<OrderDto> orderList = OrderDBUtil.queryByRestName(this.userDto.getRestaurantName());
        JComboBox<String> c = new JComboBox<>();
        // get all del
        List<UserDto> userList = UserDBUtil.queryByOrg(OrganizationType.Delivery.getValue());
        for (UserDto userDto : userList) {
            c.addItem(userDto.getUserName());
        }
        for (int i = 0; i < orderList.size(); i++) {
            Object[] data = new Object[5];
            data[0] = orderList.get(i).getMeal();
            data[1] = orderList.get(i).getUserName();
            data[2] = orderList.get(i).getDeliveryName();
            data[3] = orderList.get(i).getStatus();
            data[4] = orderList.get(i).getId();
            model.addRow(data);
        }
        workRequestJTable.getColumnModel().getColumn(2).setCellEditor(new DefaultCellEditor(c));
    }

