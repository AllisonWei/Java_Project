    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // add your handling code here:
        int i = workRequestJTable.getSelectedRow();
        if (i < 0) {
            JOptionPane.showMessageDialog(null, "Please select a line.");
            return;
        }
        String delName = (String)workRequestJTable.getModel().getValueAt(i, 2);
        if (delName.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please select a deliveryman.");
            return;
        }
        String id = (String)workRequestJTable.getModel().getValueAt(i, 4);
        OrderDto orderDto = OrderDBUtil.queryById(id);
        if (!orderDto.getStatus().equals("ordered")) {
            return;
        }
        orderDto.setDeliveryName(delName);
        orderDto.setStatus("Pending");
        OrderDBUtil.update(orderDto);
        workRequestJTable.getModel().setValueAt("Pending", i, 3);
        JOptionPane.showMessageDialog(null, "Accept order is success.");
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // add your handling code here:
        int i = workRequestJTable.getSelectedRow();
        if (i < 0) {
            JOptionPane.showMessageDialog(null, "Please select a line.");
            return;
        }
        String id = (String)workRequestJTable.getModel().getValueAt(i, 4);
        OrderDto orderDto = OrderDBUtil.queryById(id);
        if (!orderDto.getStatus().equals("ordered")) {
            return;
        }
        orderDto.setDeliveryName("");
        orderDto.setStatus("Rejected");
        OrderDBUtil.update(orderDto);
        workRequestJTable.getModel().setValueAt("Rejected", i, 3);
        JOptionPane.showMessageDialog(null, "Rejected order is success.");
    }//GEN-LAST:event_jButton3ActionPerformed


