    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       //ADD
       //格式化日期
       
       if(txtBloodP.getText().length()<=0|| 
          txtBodyT.getText().length()<=0|| 
          txtPulse.getText().length()<=0 ){
            JOptionPane.showMessageDialog(null, "Sorry，text box can not be empty.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
       }
       
       boolean validFlag = true;
       String validStr="";
       boolean isMatch;
       
       //BloodPressure
       isMatch = Pattern.matches("^\\d{2,3}+(\\/\\d{2,3}+)+$", txtBloodP.getText());
       if(!isMatch){
           validFlag = false;
           validStr+="[BloodPressure]";
       }
       
       //BodyTemperature
       isMatch = Pattern.matches("^\\d{2}+(\\.\\d{1}+)+$", txtBodyT.getText());
       if(!isMatch){
           validFlag = false;
           validStr+="[BodyTemperature]";
       }
       
       //pulse
       isMatch = Pattern.matches("\\d{2}", txtPulse.getText());
       if(!isMatch){
           validFlag = false;
           validStr+="[Pulse]";
       }
       if(!validFlag){
            JOptionPane.showMessageDialog(this, "Sorry，please enter a valid value."+validStr);
            return;
       }
       
       Encounter e=new Encounter();
       
       e.setBloodPressure(txtBloodP.getText());
       e.setBodyTemperature(Double.valueOf(txtBodyT.getText()));
       
       //SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
       e.setDate(txtDate.getText());
       e.setPulse(Integer.valueOf(txtPulse.getText()));
       
       Patient patient=AppData.getPatient(cityName, communityName, houseName, name);
       
       patient.getEncounterHistory().getEncounters().add(e);
        JOptionPane.showMessageDialog(null, "Add successfully", "successfully", JOptionPane.CLOSED_OPTION);
    }//GEN-LAST:event_jButton1ActionPerformed

