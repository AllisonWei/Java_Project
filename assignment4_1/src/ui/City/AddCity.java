    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
        if(txtCityName.getText().length()<=0){
            JOptionPane.showMessageDialog(null, "Sorry，text box can not be empty.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        boolean validFlag = true;
        String validStr="";
        boolean isMatch;
        
        isMatch = Pattern.matches("^[A-Za-z]+$", txtCityName.getText());
        if (!isMatch){
            validFlag=false;
            validStr+="[CityName] ";
        }
        
        if(!validFlag){
             JOptionPane.showMessageDialog(this, "Sorry，please enter a valid value."+validStr);
             return;
        }

            City c=new City();
            c.setCityName(txtCityName.getText());
            AppData.addCity(c);
            
            JOptionPane.showMessageDialog(null, "Add successfully", "successfully", JOptionPane.CLOSED_OPTION);
        
        
    }//GEN-LAST:event_btnAddActionPerformed

